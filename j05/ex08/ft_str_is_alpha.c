int ft_str_is_alpha(char *str)
{
	int i;

	i = 0;
	while ((str[i] != '\0' && str[i] >= 'a' && str[i] <= 'z') || (str[i] >= 'A'
			&& str[i] <= 'Z') || (str[i] >= '0' && str[i] <= '9'))
		i += 1;
	if (str[i] == '\0')
		return (1);
	return (0);
}
