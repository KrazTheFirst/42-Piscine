int					ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	x;

	if (n)
	{
		x = 0;
		while (s1[x] == s2[x] && s1[x] != '\0' && s2[x] != '\0' && x != (n - 1))
			x += 1;
		return (s1[x] - s2[x]);
	}
	return (0);
}
