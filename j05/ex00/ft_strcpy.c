char		*ft_strcpy(char *dest, char *src)
{
	int		x;

	x = 0;
	while (src[x] != '\0')
	{
		dest[x] = src[x];
		x = x + 1;
	}
	*(dest + x) = *(src + x);
	return (dest);
}
