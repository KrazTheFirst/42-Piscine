int ft_str_is_lowercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0' && str[i] >= 'a' && str[i] <= 'z')
		i += 1;
	if (str[i] == '\0')
		return (1);
	return (0);
}
