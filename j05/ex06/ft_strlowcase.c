char *ft_strlowcase(char *str)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] >= 'A' && str[i] <= 'Z')
				str[i] = ((str[i] - 'Z') + 'a');
			i += 1;
		}
	}
	return (str);
}
