int		ft_count_if(char **tab, int(*f)(char*))
{
	int	i;
	int	x;

	i = 0;
	x = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			x += 1;
		i += 1;
	}
	return (i);
}
