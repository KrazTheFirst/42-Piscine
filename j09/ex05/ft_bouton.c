int ft_bouton(int i, int j, int k)
{
	int res;

	if ((i >= j && j >= k) || (i <= j && j <= k))
		res = j;
	else if ((j >= k && k >= i) || (j <= k && k <= i))
		res = k;
	else if ((k >= i && i >= j) || (k <= i && i <= j))
		res = i;
	return (res);
}
