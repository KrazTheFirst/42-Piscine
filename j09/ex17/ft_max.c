int ft_max(int *tab, int length)
{
	int i;
	int j;

	i = 0;
	if (length > 0)
		j = tab[i];
	while (i < length)
	{
		if (tab[i] > j)
			j = tab[i];
		i++;
	}
	return (j);
}
